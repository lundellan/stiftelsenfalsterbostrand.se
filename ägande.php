<h3 class='caption captionLarge textTitle'>Strandskyddets omfattning</h3>
<p class='text'>Strandskyddet är till för att ge alla människor tillgång till stränderna, och för att bevara goda förutsättningar för ett rikt växt- och djurliv på land och i vatten. Strandskyddet gäller samtliga stränder vid havet, insjöar och vattendrag oavsett storlek.
Strandskyddszonen omfattar både land och vatten, inklusive undervattensmiljön. Den utgår från strandkanten och sträcker sig vanligtvis 100 meter i båda riktningarna. I Vellinge kommun har strandskyddet utökats till 300 m längs kusten.</p>
	
	
<h3 class='caption captionLarge textTitle'>Strandskyddets regler</h3>
<p class='text'>Det är miljöbalken som reglerar vad man får och inte får göra inom strandskyddsområde. Inom strandskyddsområde får inte:</p>

<ul>
  	<li>nya byggnader uppföras</li>
  	<li>byggnader eller byggnaders användning ändras eller andra anläggningar eller anordningar utföras, om det hindrar eller avhåller allmänheten från att beträda ett område där den annars skulle ha fått färdas fritt</li>
    <li>grävningsarbeten eller andra förberedelsearbeten utföras för byggnader, anläggningar eller anordningar som avses ovan</li>
    <li>åtgärder vidtas som väsentligt förändrar livsvillkoren för djur- eller växtarter.</li>
</ul>

<p class='text'>Förbuden gäller inte byggnader, anläggningar, anordningar eller åtgärder som inte avser att tillgodose bostadsändamål som behövs för jordbruket, fisket eller skogsbruket och om de till sin funktion måste finnas inom området för strandskydd.</p>

