	<h3 class='caption captionLarge textTitle'>Stiftelsens historia</h3>
	<p class='text margin25'>Stiftelsen Falsterbo Strand bildades 1947 av Malmö-Trelleborg 
Järnvägs AB i likvidation. Bolagsstämman i järnvägsbolaget hade 
före likvidationen fattat beslut om att skänka hela återstoden av 
det s.k. Västra området till stiftelsen.</p>
	
	<p class='text margin25'>Malmö-Trelleborg Järnvägs AB ägde två stora områden i Falsterbo, 
nämligen dels det Västra området (området mellan Falsterbo golfklubb, 
Reinhold von Essens väg, Fyrvägen, Stadsallén, Södergatan, Sjögatan 
och Östersjön) och dels det Östra området (Strandbaden). Det sistnämnda 
området avyttrades i samband med järnvägens förstatligande 1943.</p>

<p class='text margin10'>Från 1902, då det Västra området förvärvades, och fram till 1947 avyttrade järnvägsbolaget ett stort antal tomter från detta område. Återstoden av det 
Västra området erhöll Stiftelsen i gåva 1947. Området omfattar marken längs 
södra sidan av Falsterbo golfklubbs drivingrange fram till Falsterbohus samt 
området mellan Falsterbohus och Sjögatans förlängning.</p>

<h3 class='caption captionLarge textTitle'>Stiftelsens ändamål</h3>
<p class='text'>Stiftelsens ändamål är att förvalta ovannämnda område och ge de fastighetsägare, vars tomter
köpts från järnvägsbolagets s.k. Västra område, tillträde till stranden, och "mot skälig arrendeavgift 
till stiftelsen vid stranden bibehålla där uppförda badhytter, så länge dessa hålles i gott och 
prydligt skick".</p>

	
